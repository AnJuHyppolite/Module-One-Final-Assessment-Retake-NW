const sumEvenNumbers = require("../problems/sumEvenNumbers")

describe("sumEvenNumbers", () => {
    test("Sums the even numbers in an array", () => {
        expect(sumEvenNumbers([99,25,31,14])).toBe(14)
        expect(sumEvenNumbers([7,29,30,30,24,0,3,0,5,5])).toBe(84)
        expect(sumEvenNumbers([30,7,27,12,22,55,6,7,6,11,25,6,91,73,2,14,6,6,1,33,15,3,7,77,21,28,77,37,5,24,36,17,31,8,32,24,0,10,9,6])).toBe(278)
        expect(sumEvenNumbers([69,5,37,37,81,19,15,30,1,24,19,14,9,13,8,13,28,36,8,3,39,39,28,24,32,25,15,28,32,6,22,36,10,18,19,29,20,71,36,18])).toBe(458)
        expect(sumEvenNumbers([97,62,15,42,40,91,41,91,14,59,73,31,51,16,83,82,30,57,17,48,83,36,9,14,83,77,99,73,66,81,47,10,85,74,11,13,11,25,55,19,43,74,17,65,54,55,7,52,55,27,82,8,76,97,49,72,87,0,98,20,9,41,53,39,71,70,96,9,22,46,33,87,12,80,59,38,90,39,74,87,21,71,99,60,83,58,98,74,41,51,98,51,44,48,88,12,62,71,80,23])).toBe(2320)
    })
})
